// Get coordinates for a province name (handles Turkish characters)
export const getProvinceCoords = (name: string): { x: number; y: number } | undefined => {
  const normalized = normalizeProvinceName(name);

  // Try direct match first
  if (provinceCoordinates[normalized]) {
    return provinceCoordinates[normalized];
  }

  // Try to find a matching key
  for (const key of Object.keys(provinceCoordinates)) {
    if (normalizeProvinceName(key) === normalized) {
      return provinceCoordinates[key];
    }
  }

  return undefined;
};
